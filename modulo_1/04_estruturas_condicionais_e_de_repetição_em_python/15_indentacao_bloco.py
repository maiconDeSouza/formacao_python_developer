# A indentação e os blocos de código em Python são elementos fundamentais que
# ajudam a organizar e estruturar o código de forma clara e legível. Em Python,
# a indentação é usada para delimitar blocos de código, como loops, condicionais,
# funções e classes. Em vez de utilizar chaves ou palavras-chave para definir o
# início e o fim de um bloco, como em algumas outras linguagens de programação,
# Python utiliza a indentação como sua principal forma de estruturação.

# Essa abordagem tem como objetivo tornar o código mais limpo e legível,
# incentivando uma boa prática de programação. A indentação consistente facilita
# a compreensão do código, tanto para o programador quanto para outros que
# possam ler o código posteriormente.

# Por exemplo, quando escrevemos um loop for em Python, a indentação define o
# bloco de código que será repetido em cada iteração do loop. Da mesma forma, em
# condicionais como if, else e elif, a indentação determina o bloco de código
# que será executado condicionalmente, dependendo da avaliação da expressão.

# Além disso, funções e classes em Python também são definidas com
# indentação. Isso significa que o corpo de uma função ou método pertence à
# função ou método se estiver indentado na mesma profundidade que a declaração da
# função ou método.

# Em resumo, a indentação em Python desempenha um papel crucial na estruturação
# do código, promovendo boas práticas de programação e melhorando a legibilidade
# do código. É uma característica fundamental da linguagem que contribui para a
# sua simplicidade e elegância.
