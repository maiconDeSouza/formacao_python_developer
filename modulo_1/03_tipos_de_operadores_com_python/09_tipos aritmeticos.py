# Operações Aritméticas:
# Adição (+):
resultado = 10 + 5  # resultado é 15

# Subtração (-):
resultado = 10 - 5  # resultado é 5

# Multiplicação (*):
resultado = 10 * 5  # resultado é 50

# Divisão (/):
resultado = 10 / 5  # resultado é 2.0 (float)

# Divisão Inteira (//):
resultado = 10 // 3  # resultado é 3 (int)

# Módulo (%):
resultado = 10 % 3  # resultado é 1

# Exponenciação ()**:
resultado = 2 ** 3  # resultado é 8


# Precedência das Operações:
# A precedência das operações aritméticas em Python segue a ordem padrão da matemática:

# Parênteses têm a precedência mais alta.
# Em seguida, exponenciação.
# Multiplicação e divisão têm a mesma precedência e são avaliados da esquerda para a direita.
# Adição e subtração têm a mesma precedência e também são avaliados da esquerda para a direita.
# Exemplo de expressão com precedência mista:


resultado = 10 + 5 * 2  # resultado é 20 (5 * 2 = 10, + 10 = 20)
